public class SomaValores {
    /*Utilizando as estruturas de repetição , construa um algoritmo
     que calcule a soma de valores inteiros existentes entre 1 e 100.
     */
    public static void main(String[] args) {
        int s=0;
        for (int cc = 1; cc <= 100; cc++)
            s=s+cc;
        System.out.println(s);



    }
}
